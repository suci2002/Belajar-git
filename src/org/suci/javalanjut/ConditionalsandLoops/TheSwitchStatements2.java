package org.suci.javalanjut.ConditionalsandLoops;

public class TheSwitchStatements2 {
    public static void main(String[] args) {
        int a = 11; int b = 12; int c = 40;
        switch (a) {
            case 40:
                System.out.println(b);
                break;
            default:
                System.out.println(c);
        }
    }
}
